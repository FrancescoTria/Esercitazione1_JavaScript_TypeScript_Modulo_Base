let str = "Ciao mondo";   // stringa
let num = 123;            // numero
let bool = true;          // booleano
let vuoto = null;         // null
let indefinito = undefined; // undefined

console.log("str:", typeof str);           // "string"
console.log("num:", typeof num);           // "number"
console.log("bool:", typeof bool);         // "boolean"
console.log("vuoto:", typeof vuoto);       // "object"  (sì, è un bug storico mai corretto)
console.log("indefinito:", typeof indefinito); // "undefined"
